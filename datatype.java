public class datatype {
    public static void main(String[] args) {
        
        int myInt = 10; // Integer
        double myDouble = 3.14; // Floating-point number
        char myChar = 'A'; // Character
        boolean myBoolean = true; // Boolean

        // Non-primitive data types (Objects)
        String myString = "Hello, World!"; // String

        // Displaying the values
        System.out.println("Integer: " + myInt);
        System.out.println("Double: " + myDouble);
        System.out.println("Character: " + myChar);
        System.out.println("Boolean: " + myBoolean);
        System.out.println("String: " + myString);


        System.out.printf("integer: %d\n", myInt);
        System.out.printf("double: %f\n", myDouble);
        System.out.printf("character: %c\n", myChar);
        System.out.printf("boolean: %b\n", myBoolean);
        System.out.printf("string: %s\n", myString);    
    }
}